package entities;

/**
 *
 * @author HUNGUYEN
 */
public class PersonnelBasic {

    private String personnelID;
    private String name;
    private String birth;
    private String sex;
    private String office;
    private String position;
    private String perKind;
    private String perContract;
    private String phone;
    private String perEmail;
    private String compEmail;

    public String getPersonnelID() {
        return personnelID;
    }

    public void setPersonnelID(String personnelID) {
        this.personnelID = personnelID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBirth() {
        return birth;
    }

    public void setBirth(String birth) {
        this.birth = birth;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getOffice() {
        return office;
    }

    public void setOffice(String office) {
        this.office = office;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getPerKind() {
        return perKind;
    }

    public void setPerKind(String perKind) {
        this.perKind = perKind;
    }

    public String getPerContract() {
        return perContract;
    }

    public void setPerContract(String perContract) {
        this.perContract = perContract;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPerEmail() {
        return perEmail;
    }

    public void setPerEmail(String perEmail) {
        this.perEmail = perEmail;
    }

    public String getCompEmail() {
        return compEmail;
    }

    public void setCompEmail(String compEmail) {
        this.compEmail = compEmail;
    }

}
